package LogicaDeProgramacao;

public class ExpressoesComparativas {
    public static void main(String[] args) {

        int pao = 10;
        int acucar = 20;

        System.out.println(pao == acucar); // o valor do pão é igual ao valor do açúcar?
        System.out.println(pao != acucar); // o valor do pão é diferente do valor do açúcar?
        System.out.println(pao > acucar); // o valor do pão é maior que o valor do açúcar?
        System.out.println(pao < acucar); // o valor do pão é menor que o valor do açúcar?
        System.out.println(pao >= acucar); // o valor do pão é maior ou igual ao valor do açúcar?
        System.out.println(pao <= acucar); //o valor do pão é menor ou igual ao valor do açúcar?


    }

}
